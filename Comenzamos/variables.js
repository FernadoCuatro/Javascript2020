//var, variables globales.
var saludo = 'Hola Anna'
if(saludo){
 var saludo = 'Hola Miguel'
 console.log(saludo);
}
console.log(saludo);

//let, variables limitadas al alcance del bloque donde lo definimos. 
let nombre = 'Anna'
if(nombre){
 let nombre = 'Cecilia'
 console.log(nombre)
}
console.log(nombre);

//const, variables inmutables
const PI = 3.14
console.log(PI);
